package array;

public class MeusPrimeirosArrays {

	public static void main(String[] args) {
		String[] nomes = new String[4];
		nomes[0] = "Douglas";
		nomes[1] = "Duke";
		nomes[2] = "Uncle bob";
		nomes[3] = "Gosling";
		double[] notas = new double[4];
		notas[0] = 6;
		notas[1] = 7;
		notas[2] = 8;
		notas[3] = 9;
		System.out.println(nomes[0] + " - " + notas[0]);
		System.out.println(nomes[1] + " - " + notas[1]);
		System.out.println(nomes[2] + " - " + notas[2]);
		System.out.println(nomes[3] + " - " + notas[3]);
	}

}
